  # 人脸集合中查找
  # options: {image: image, group_id: group_id, ext_filelds: ext_fields, user_top_num: user_top_num}
  def identify
    info = User.identify({image: Base64.encode64(params[:image].read), group_id: "default", ext_fields: {}, user_top_num: 1})

    render :json => info
  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_user
      @user = User.find(params[:id])
    end

    # Never trust parameters from the scary internet, only allow the white list through.
    def user_params
      params.require(:user).permit(:name, :file)
    end
end
